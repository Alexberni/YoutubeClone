    private void jTable1KeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTable1KeyPressed
        int key = evt.getKeyCode();
        if (key == KeyEvent.VK_ENTER) {
            String titulo = (String) this.jTable1.getValueAt(this.jTable1.getSelectedRow(), this.jTable1.getSelectedColumn());
            System.out.println("Video Selected: " + titulo);
            this.controller.getVideo(titulo);
        }      
    }//GEN-LAST:event_jTable1KeyPressed

    private void subirVideoItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_subirVideoItemActionPerformed
        File fichero = null;
        JFileChooser jFileChooser1 = new JFileChooser();
        int seleccion=jFileChooser1.showOpenDialog(this);
        if(seleccion==jFileChooser1.APPROVE_OPTION){
            fichero=jFileChooser1.getSelectedFile();
            System.out.println("Elijo fichero");
            System.out.println("Tama√±o: "+ fichero.length());
        }
        jFileChooser1.setVisible(false);
        this.controller.uploadVideo(fichero.getPath());       
    }//GEN-LAST:event_subirVideoItemActionPerformed

